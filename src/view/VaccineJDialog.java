    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String vaccineName = txtType.getText();
        String vaccineQuanlity = txtQuanlity.getText();
        String vaccineLo = txtLo.getText();
        String vaccineDate = txtDate.getText();
        
        Vaccine vac = new Vaccine(vaccineName, vaccineQuanlity, vaccineLo, vaccineDate);
        VaccineDAO.insert(vac);
        
        showVaccine();
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedIndex = tblVaccine.getSelectedRow();
        if(selectedIndex >=0){
            Vaccine vac = vaccineList.get(selectedIndex);
            int option = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa dòng này");
            
            //option = 0 là yes, option=1 là no, option=2 là cancel
            if(option == 0){
                VaccineDAO.delete(vac.getId());
                showVaccine();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String input = JOptionPane.showInputDialog(this, "Nhập số lô để tìm kiếm");
        if(input.length() > 0){
            vaccineList = VaccineDAO.search(input);
            
            tableModel.setRowCount(0);
            
            vaccineList.forEach((vaccine)->{
                tableModel.addRow(new Object[] {
                    tableModel.getRowCount()+1,
                    vaccine.getVaccineName(),
                    vaccine.getVaccineQuanlity(),
                    vaccine.getVaccineLo(),
                    vaccine.getVaccineDate()
                });
            });
        }
    }//GEN-LAST:event_btnSearchActionPerformed

