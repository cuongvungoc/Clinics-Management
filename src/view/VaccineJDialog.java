    private void showVaccine(){
        vaccineList = VaccineDAO.findAll();
        tableModel.setRowCount(0); //xóa toàn bộ dữ liệu trong bảng
        vaccineList.forEach(vaccine -> {//for(Vaccine vaccine:vaccineList)
            tableModel.addRow(new Object[] {
                tableModel.getRowCount()+1,
                vaccine.getVaccineName(),
                vaccine.getVaccineQuanlity(),
                vaccine.getVaccineLo(),
                vaccine.getVaccineDate()
            });
        });
    }

